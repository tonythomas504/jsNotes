//* ASSIGNMENT OPERATOR
let r = 5;//* = is an assignment operator

//* ADDITION ASSIGNMENT
r += 1; //? r = r + 1

//* SUBTRACTION ASSIGNMENT
r -= 1; //? r = r - 1

//* MULTIPLICATION ASSIGNMENT
r *= 1; //? r = r * 1

//* DIVISION ASSIGMENT
r/= 1; //? r = r / 1

//* REMAINDER (modulus) ASSIGNMENT
r %= 1; //? r = r % 1

//* EXPONETIAL ASSIGNMENT 
r **= 1; //? r = r ** 1 (to the power of)
