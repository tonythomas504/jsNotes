//* EQUAL
console.log('3' == 3); 
console.log(3 == 3);
console.log('3' == '3');
//? == compares the value doesn't care if its a string or value. 

//* STRICT EQUAL
console.log('3' === 3);
console.log(3 === 3);
//? only accepts value of items of the same value. String = String. Number = Number.

//* NOT EQUAL COMPARISON
console.log('3' != 3); //? ! = bang
//? != compares like equal comparison operator. strings and values can be equal to each other. 

//* STRICT NOT EQUAL 
console.log('3' !== 3); 
//? !== operates like strict equal operator. strings = strings. Values = values. Values do not equal strings

